def missing_values_table(dataframe, na_name=False):
    na_columns = [col for col in dataframe.columns if dataframe[col].isnull().sum() > 0]
    n_miss = dataframe[na_columns].isnull().sum().sort_values(ascending=False)
    ratio = (dataframe[na_columns].isnull().sum() / dataframe.shape[0] * 100).sort_values(ascending=False)
    missing_df = pd.concat([n_miss, np.round(ratio, 2)], axis=1, keys=['n_miss', 'ratio'])
    print(missing_df, end="\n")
    if na_name:
        return na_columns

na_columns = missing_values_table(df, na_name=True)

def fill_missing_values(dataframe, na_columns):
    """
    DataFrame içindeki eksik değerleri sütunların medianı ile doldurur.

    Parameters:
    dataframe (DataFrame): Eksik değerlerin doldurulacağı DataFrame.
    na_columns (list): Eksik değerleri içeren sütunların listesi.

    Returns:
    None
    """
    for column in na_columns:
        median_value = dataframe[column].median()
        dataframe[column].fillna(median_value, inplace=True)

# Eksik değerleri doldurma işlemi
